# update method ,we can adding the items{keys:values}
laptops={'hp':10000,'dell':20000,'acer':15000,'lenovo':12000,'apple':80000}
print(laptops)
# {'hp': 10000, 'dell': 20000, 'acer': 15000, 'lenovo': 12000, 'apple': 80000}

laptops.update({'realme':12000,'mi':13000})
print(laptops)

# {'hp': 10000, 'dell': 20000, 'acer': 15000, 'lenovo': 12000, 'apple': 80000, 'realme': 12000, 'mi': 13000}

